<template><div><h1 id="croziflette" tabindex="-1"><a class="header-anchor" href="#croziflette" aria-hidden="true">#</a> Croziflette</h1>
<p><img src="https://assets.afcdn.com/recipe/20191231/105964_w648h414c1cx2150cy1433cxt0cyt0cxb4300cyb2867.jpg" alt="Croziflette"></p>
<h2 id="ingredients" tabindex="-1"><a class="header-anchor" href="#ingredients" aria-hidden="true">#</a> Ingrédients</h2>
<p><strong>Pour 3/4 personnes</strong></p>
<ul>
<li>300 g de crozet au sarrasin</li>
<li>200 g de lardons</li>
<li>20 cl de crème fraîche</li>
<li>1 oignon</li>
<li>1 reblochon</li>
</ul>
<h2 id="preparation" tabindex="-1"><a class="header-anchor" href="#preparation" aria-hidden="true">#</a> Préparation</h2>
<ul>
<li>Hacher l'oignon.</li>
</ul>
<h2 id="cuisson-35-minutes" tabindex="-1"><a class="header-anchor" href="#cuisson-35-minutes" aria-hidden="true">#</a> Cuisson (35 minutes)</h2>
<ul>
<li>Faire cuire les crozets dans l'eau bouillante salée pendant 20 minutes.</li>
<li>Au bout de 10 minutes de cuisson des crozets, couper l'oignon et le faire revenir dans du beurre.Faites la caraméliser.</li>
<li>Mettre les oignons de coté et faire revenir les lardons.</li>
<li>Mettre les oignons cuits lardons crème dans la poêle et faire réduire.</li>
<li>Égoutter les crozets.</li>
<li>Mettre dans le plat à gratin une couche de crozet, une couche de crème/lardons/oignons.</li>
<li>Remplir le plat de cette manière et mettre au dessus le reblochon coupé en 2.</li>
<li>Faire cuire au four pendant 20 minutes à 200°C.</li>
</ul>
</div></template>


