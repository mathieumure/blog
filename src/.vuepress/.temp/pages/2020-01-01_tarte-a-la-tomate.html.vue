<template><div><h1 id="tarte-a-la-tomate" tabindex="-1"><a class="header-anchor" href="#tarte-a-la-tomate" aria-hidden="true">#</a> Tarte à la tomate</h1>
<p><img src="https://assets.afcdn.com/recipe/20160325/56973_w648h414c1cx1500cy1000.jpg" alt="Tarte à la tomate"></p>
<h2 id="ingredients" tabindex="-1"><a class="header-anchor" href="#ingredients" aria-hidden="true">#</a> Ingrédients</h2>
<p><strong>Pour 3/4 personnes</strong></p>
<ul>
<li>1 pâte feuilletée</li>
<li>100 g de gruyère râpé</li>
<li>8 tomates</li>
<li>Moutarde</li>
<li>Herbes de Provence</li>
</ul>
<h2 id="preparation" tabindex="-1"><a class="header-anchor" href="#preparation" aria-hidden="true">#</a> Préparation</h2>
<ul>
<li>Couper vos tomates en rondelles</li>
</ul>
<h2 id="cuisson-35-minutes" tabindex="-1"><a class="header-anchor" href="#cuisson-35-minutes" aria-hidden="true">#</a> Cuisson (35 minutes)</h2>
<ul>
<li>Disposer la pâte dans un moule à tarte.</li>
<li>Etaler la moutarde en couche fine sur toute la surface de la pâte.</li>
<li>Ajouter les tranches de raclette afin que cela tapisse entièrement le moule.</li>
<li>Plonger les tomates dans l'eau bouillante pendant moins d'1 minute dans l'eau afin de retirer la peau plus facilement puis les couper en rondelles très fines. Les disposer dans le plat.</li>
<li>Ajouter les herbes de Provence.</li>
<li>Cuire à 180°C pendant 30 minutes environ.</li>
</ul>
</div></template>


