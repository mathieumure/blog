<p><img src="https://assets.afcdn.com/recipe/20130910/5819_w648h414c1cx1728cy2392.jpg" alt="Pâte à crêpes"></p>
<h2 id="ingredients" tabindex="-1"><a class="header-anchor" href="#ingredients" aria-hidden="true">#</a> Ingrédients</h2>
<p><strong>Pour 20 crèpes</strong></p>
<ul>
<li>5 oeufs</li>
<li>500 g de farine</li>
<li>1 l de lait demi-écrémé</li>
<li>3 cuillères à soupe d'huile</li>
<li>1/2 verre de bière</li>
</ul>
<h2 id="cuisson-35-minutes" tabindex="-1"><a class="header-anchor" href="#cuisson-35-minutes" aria-hidden="true">#</a> Cuisson (35 minutes)</h2>
<ul>
<li>Dans un saladier verser la moitié de la farine,</li>
<li>Ajouter les oeufs un à un</li>
<li>Ajouter ensuite un peu de lait puis de la farine puis à nouveau du lait et ainsi de suite.</li>
<li>Enfin rajouter l'huile et la bière.</li>
<li>Laisser reposer 1 heure, votre pâte est prête.</li>
</ul>
