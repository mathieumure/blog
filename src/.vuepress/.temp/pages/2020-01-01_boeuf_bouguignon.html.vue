<li>800 g de boeuf pour bourguignon</li>
<li>100 g de lardons</li>
<li>50 g de beurre ou 3 cuillères à soupe d'huile</li>
<li>75 cL de vin rouge</li>
<li>2 oignons</li>
<li>1 gousse d'ail</li>
