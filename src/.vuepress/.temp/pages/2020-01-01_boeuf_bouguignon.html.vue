<li>Ajouter les oignons, les champignons égouttés et les carottes. Mélanger et laisser dorer un instant.</li>
<li>Mouiller avec le vin rouge qui doit recouvrir la viande.</li>
<li>Saler et poivrer.</li>
<li>Ajouter l’ail et le bouquet garni.</li>
<li>Fermer la cocotte minute.</li>
<li>Laisser cuire doucement 60 min à partir de la mise en rotation de la soupape.</li>
