<template><div><h1 id="tartiflette" tabindex="-1"><a class="header-anchor" href="#tartiflette" aria-hidden="true">#</a> Tartiflette</h1>
<p><img src="https://assets.afcdn.com/recipe/20160401/38946_w648h414c1cx2690cy1793.jpg" alt="Tartiflette"></p>
<h2 id="ingredients" tabindex="-1"><a class="header-anchor" href="#ingredients" aria-hidden="true">#</a> Ingrédients</h2>
<p><strong>Pour 3/4 personnes</strong></p>
<ul>
<li>1 kg de pomme de terre</li>
<li>200 g de lardons fumés</li>
<li>200 g d'oignon émincés</li>
<li>1 reblochon</li>
<li>2 cuillères à soupe d'huile</li>
<li>1 gousse d'ail</li>
</ul>
<h2 id="preparation" tabindex="-1"><a class="header-anchor" href="#preparation" aria-hidden="true">#</a> Préparation</h2>
<ul>
<li>Éplucher les pommes de terre, les couper en dés, bien les rincer et les essuyer dans un torchon propre.</li>
<li>Précuire les pommes de terre à la vapeur.</li>
<li>Émincer l'oignon.</li>
</ul>
<h2 id="cuisson-45-minutes" tabindex="-1"><a class="header-anchor" href="#cuisson-45-minutes" aria-hidden="true">#</a> Cuisson (45 minutes)</h2>
<ul>
<li>Faire chauffer l'huile dans une poêle, y faire fondre les oignons.</li>
<li>Lorsque les oignons sont fondus, ajouter les pommes de terre et les faire dorer de tous les côtés.</li>
<li>Lorsqu'elles sont dorées, ajouter les lardons et finir de cuire.</li>
<li>D'autre part, couper le reblochon en quatre.</li>
<li>Préchauffer le four à 200°C (thermostat 6-7) et préparer un plat à gratin en frottant le fond et les bords avec la gousse d'ail épluchée.</li>
<li>Dans le plat à gratin, étaler une couche de pommes de terre aux lardons, disposer dessus la moitié du reblochon, puis de nouveau des pommes de terre. Terminer avec le reste du reblochon (croûte vers les pommes de terre).</li>
<li>Enfourner pour environ 20 minutes de cuisson.</li>
</ul>
