<template><div><h1 id="gratin-de-pates-au-chorizo-et-curry" tabindex="-1"><a class="header-anchor" href="#gratin-de-pates-au-chorizo-et-curry" aria-hidden="true">#</a> Gratin de pâtes au chorizo et curry</h1>
<p><img src="https://assets.afcdn.com/recipe/20130328/4186_w648h414c1cx1000cy1258.jpg" alt="Gratin de pâtes au chorizo et curry"></p>
<h2 id="ingredients" tabindex="-1"><a class="header-anchor" href="#ingredients" aria-hidden="true">#</a> Ingrédients</h2>
<p><strong>Pour 3/4 personnes</strong></p>
<ul>
<li>500 g de penne</li>
<li>1 chorizo</li>
<li>1 oignon</li>
<li>20 cl de crème fraîche fluide</li>
<li>1 oeuf</li>
<li>50 g de gruyère rapé</li>
<li>Vinaigre balsamique</li>
<li>Curry</li>
</ul>
<h2 id="preparation" tabindex="-1"><a class="header-anchor" href="#preparation" aria-hidden="true">#</a> Préparation</h2>
<ul>
<li>Hacher l'oignon.</li>
<li>Couper le chorizo en tranches puis chaque tranche en 4 morceaux.</li>
</ul>
<h2 id="cuisson-35-minutes" tabindex="-1"><a class="header-anchor" href="#cuisson-35-minutes" aria-hidden="true">#</a> Cuisson (35 minutes)</h2>
<ul>
<li>Faîtes cuire les pâtes.</li>
<li>Pendant la cuisson, jetez le chorizo et l'oignon dans une poêle avec un peu de beurre, et faites revenir à feu doux juqu'à ce que l'oignon devienne translucide.</li>
<li>Augmentez légèrement le feu, jetez un gros trait de vinaigre dans la poêle tout en remuant vivement la préparation.</li>
<li>Ajoutez la crème fraîche et laissez cuire à feu moyen jusqu'à ce que la crème prenne une couleur rouille homogène.</li>
<li>Egouttez les penne, placez-les dans un plat allant au four. Ajoutez un jaune d'oeuf puis saupoudrez de curry et mélanger de manière à répartir l'épice.</li>
<li>Rajoutez le contenu de la poêle et mélangez le tout.</li>
<li>Ajoutez le gruyère (selon votre goût) puis saupoudrez de chapelure.</li>
<li>Enfournez le tout 5 à 10 minutes à four très chaud (240°C - thermostat 8), jusqu'à ce que le plat soit bien gratiné.</li>
</ul>
</div></template>


