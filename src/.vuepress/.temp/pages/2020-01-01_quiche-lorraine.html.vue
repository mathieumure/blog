<p><strong>Pour 3/4 personnes</strong></p>
<ul>
<li>200 g de pâte brisée</li>
<li>200 g de lardons</li>
<li>30 g de beurre</li>
<li>3 oeufs</li>
<li>20 cl de crème fraîche</li>
<li>20 cl de lait</li>
<li>Muscade</li>
</ul>
<h2 id="cuisson-35-minutes" tabindex="-1"><a class="header-anchor" href="#cuisson-35-minutes" aria-hidden="true">#</a> Cuisson (35 minutes)</h2>
<p>Préchauffer le four à 180°C (thermostat 6).</p>
<ul>
<li>Etaler la pâte dans un moule, et la piquer à la fourchette.</li>
<li>Parsemer de copeaux de beurre.</li>
<li>Faire rissoler les lardons à la poêle.</li>
<li>Battre les oeufs, la crème fraîche et le lait.</li>
<li>Ajouter les lardons.</li>
<li>Assaisonner de sel, de poivre et de muscade.</li>
<li>Verser sur la pâte.</li>
<li>Cuire 45 à 50 min.</li>
<li>C'est prêt</li>
<li>Déguster</li>
</ul>
